import numpy as np
import math

# -------------------------------------------------
# 1. Задана функція вологості ґрунту
# -------------------------------------------------

def M(t):
    return 50 * math.exp(-0.1 * t) + 5 * math.sin(t)


# Аналітична похідна
def dM_exact(t):
    return -5 * math.exp(-0.1 * t) + 5 * math.cos(t)


# Центральна різниця
def central_diff(f, t, h):
    return (f(t + h) - f(t - h)) / (2 * h)


# -------------------------------------------------
# Початкові дані
# -------------------------------------------------

t0 = 1

exact = dM_exact(t0)

print("=== 1. Аналітичне рішення ===")
print("Точне значення похідної:", exact)


# -------------------------------------------------
# 2. Дослідження похибки залежно від кроку
# -------------------------------------------------

print("\n=== 2. Дослідження залежності похибки від кроку ===")

steps = [0.1, 0.01, 0.001]

best_error = float("inf")
best_h = None

for h in steps:
    approx = central_diff(M, t0, h)
    error = abs(exact - approx)

    print(f"h = {h}  D(h) = {approx:.6f}  error = {error:.6f}")

    if error < best_error:
        best_error = error
        best_h = h


print("\nОптимальний крок:", best_h)
print("Досягнута точність:", best_error)


# -------------------------------------------------
# 3. Приймаємо оптимальний крок
# -------------------------------------------------

h = best_h

print("\n=== 3. Прийнятий крок ===")
print("h =", h)


# -------------------------------------------------
# 4. Два кроки сітки
# -------------------------------------------------

D_h = central_diff(M, t0, h)
D_h2 = central_diff(M, t0, h / 2)

print("\n=== 4. Значення похідної для двох кроків ===")
print("D(h) =", D_h)
print("D(h/2) =", D_h2)


# -------------------------------------------------
# 5. Похибка при кроці h
# -------------------------------------------------

error_h = abs(exact - D_h)

print("\n=== 5. Похибка при кроці h ===")
print("error =", error_h)


# -------------------------------------------------
# 6. Метод Рунге–Ромберга
# -------------------------------------------------

print("\n=== 6. Метод Рунге–Ромберга ===")

p = 2  # порядок центральної різниці

D_RR = D_h2 + (D_h2 - D_h) / (2**p - 1)

error_RR = abs(exact - D_RR)

print("Уточнене значення:", D_RR)
print("Похибка:", error_RR)


# -------------------------------------------------
# 7. Метод Ейткена
# -------------------------------------------------

print("\n=== 7. Метод Ейткена ===")

D_h4 = central_diff(M, t0, h / 4)

D_Aitken = D_h - ((D_h2 - D_h)**2) / (D_h4 - 2 * D_h2 + D_h)

error_Aitken = abs(exact - D_Aitken)

# оцінка порядку точності

p_est = math.log(
    abs((D_h4 - D_h2) / (D_h2 - D_h))
) / math.log(2)


print("Уточнене значення:", D_Aitken)
print("Похибка:", error_Aitken)
print("Оцінка порядку точності:", p_est)


# -------------------------------------------------
# 8. Аналіз режиму поливу
# -------------------------------------------------

print("\n=== 8. Аналіз режиму поливу ===")

if D_Aitken < 0:
    print("Вологість зменшується → потрібно увімкнути полив")
else:
    print("Вологість достатня → полив не потрібен")